var React = require('react')
var ReactDOM = require('react-dom')
var ShowList = React.createClass({
  render: function(){
    var listItems = this.props.names.map(function(friend){
      return <li> {friend} </li>;
    });
    return (
      <div>
        <h3> Friends </h3>
        <ul>
          {listItems}
        </ul>
      </div>
    )
  }
});
ReactDOM.render(<ShowList />, document.getElementById('app'));
